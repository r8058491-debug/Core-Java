package operator_ques;

public class shiftoperator {
	public static void main(String[]args) {
		int a=5;
		a<<=2;//2 ki power 2 matlb 4=5*4--> 20
		System.out.println(a);
		a>>=3;//2 ki power 3 matalab 8=20/8--->2 (quotient)
		System.out.println(a);
	}

}
// <<   =  Left shift operator(2 ki power ban jaayega or value se multiply hoga)
// >>   =  Right shift operator (2 ki power ban jaayega or value se divide hoga)